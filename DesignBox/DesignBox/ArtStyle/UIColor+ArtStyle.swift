import UIKit

extension UIColor {
    
    public class func art_mainGrayColor() ->UIColor {
        return UIColor.art_colorWithHexString(hexString: "1C1C1C")
    }
    
    public class func art_mainHighColor() ->UIColor {
        return UIColor.art_colorWithHexString(hexString: "FB1E36")
    }
    
}
